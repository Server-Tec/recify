              style: const TextStyle(fontSize: 16),
            ),
            Text(
              'Kohlenhydrate: ${recipe.carbs}g',
              style: const TextStyle(fontSize: 16),
            ),
            Text('Fett: ${recipe.fat}g', style: const TextStyle(fontSize: 16)),
            const SizedBox(height: 24),
            ElevatedButton.icon(
              onPressed: () => _launchDonation(recipe.donateUrl),
              icon: const Icon(Icons.volunteer_activism),
              label: const Text('Unterstütze dieses Rezept'),
              style: ElevatedButton.styleFrom(
                minimumSize: const Size.fromHeight(52),
              ),
            ),
            const SizedBox(height: 12),
            const Text(
              'Jede Unterstützung hilft uns, Recify weiter zu verbessern.',
              style: TextStyle(fontSize: 14, color: Colors.black54),
            ),
          ],
        ),
      ),
    );
  }

  static Future<void> _launchDonation(String url) async {
    final uri = Uri.parse(url);
    if (await canLaunchUrl(uri)) {
      await launchUrl(uri, mode: LaunchMode.externalApplication);
    }
  }
}

class FavoritesPage extends StatelessWidget {
  final List<String> favorites;
  final Function(String) onToggleFavorite;

  const FavoritesPage({
    super.key,
    required this.favorites,
    required this.onToggleFavorite,
  });

  @override
  Widget build(BuildContext context) {
    final favoriteRecipes = recipes
        .where((recipe) => favorites.contains(recipe.id))
        .toList();

    return Scaffold(
      appBar: AppBar(title: const Text('Favoriten')),
      body: favoriteRecipes.isEmpty
          ? const Center(child: Text('Keine Favoriten gespeichert'))
          : ListView.builder(
              padding: const EdgeInsets.all(16),
              itemCount: favoriteRecipes.length,
              itemBuilder: (context, index) {
                final recipe = favoriteRecipes[index];
                return RecipeCard(
                  recipe: recipe,
                  isFavorite: true,
                  onToggleFavorite: onToggleFavorite,
                );
              },
            ),
    );
  }
}

