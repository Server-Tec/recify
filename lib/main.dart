class RecipeHomePage extends StatefulWidget {
  const RecipeHomePage({super.key});

  @override
  State<RecipeHomePage> createState() => _RecipeHomePageState();
}

class _RecipeHomePageState extends State<RecipeHomePage> {
  int selectedIndex = 0;
  String searchQuery = '';
  String selectedCategory = 'Alle';
  List<String> favorites = [];

  void onItemTapped(int index) {
    setState(() {
      selectedIndex = index;
    });
  }

  void updateSearch(String query) {
    setState(() {
      searchQuery = query;
    });
  }

  void updateCategory(String category) {
    setState(() {
      selectedCategory = category;
    });
  }

  void toggleFavorite(String recipeId) {
    setState(() {
      if (favorites.contains(recipeId)) {
        favorites.remove(recipeId);
      } else {
        favorites.add(recipeId);
      }
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: selectedIndex == 0
          ? RecipeListPage(
              searchQuery: searchQuery,
              selectedCategory: selectedCategory,
              favorites: favorites,
              onToggleFavorite: toggleFavorite,
            )
          : FavoritesPage(
              favorites: favorites,
              onToggleFavorite: toggleFavorite,
            ),
      bottomNavigationBar: NavigationBar(
        selectedIndex: selectedIndex,
        onDestinationSelected: onItemTapped,
        destinations: const [
          NavigationDestination(icon: Icon(Icons.home), label: 'Home'),
          NavigationDestination(icon: Icon(Icons.favorite), label: 'Favoriten'),
        ],
      ),
      floatingActionButton: selectedIndex == 0
          ? FloatingActionButton(
              onPressed: () => _showFilterDialog(context),
              child: const Icon(Icons.filter_list),
            )
          : null,
    );
  }

  void _showFilterDialog(BuildContext context) {
    showDialog(
      context: context,
      builder: (context) => AlertDialog(
        title: const Text('Filter & Suche'),
        content: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            TextField(
              decoration: const InputDecoration(
                labelText: 'Suche nach Rezepten',
                prefixIcon: Icon(Icons.search),
              ),
              onChanged: updateSearch,
            ),
            const SizedBox(height: 16),
            DropdownButtonFormField<String>(
              initialValue: selectedCategory,
              decoration: const InputDecoration(labelText: 'Kategorie'),
              items: categories.map((category) {
                return DropdownMenuItem(value: category, child: Text(category));
              }).toList(),
              onChanged: (value) => updateCategory(value!),
            ),
          ],
        ),
        actions: [
          TextButton(
            onPressed: () => Navigator.of(context).pop(),
            child: const Text('Schließen'),
          ),
        ],
      ),
    );
  }
}

class RecipeListPage extends StatelessWidget {
  final String searchQuery;
  final String selectedCategory;
  final List<String> favorites;
  final Function(String) onToggleFavorite;

  const RecipeListPage({
    super.key,
    required this.searchQuery,
    required this.selectedCategory,
    required this.favorites,
    required this.onToggleFavorite,
  });

  @override
  Widget build(BuildContext context) {
    final filteredRecipes = recipes.where((recipe) {
      final matchesSearch =
          recipe.title.toLowerCase().contains(searchQuery.toLowerCase()) ||
          recipe.subtitle.toLowerCase().contains(searchQuery.toLowerCase());
      final matchesCategory =
          selectedCategory == 'Alle' || recipe.category == selectedCategory;
      return matchesSearch && matchesCategory;
    }).toList();

    return Scaffold(
      appBar: AppBar(
        title: const Text('Recify'),
        centerTitle: false,
        actions: [
          IconButton(
            icon: const Icon(Icons.search),
            onPressed: () => _showSearchDialog(context),
          ),
        ],
      ),
      body: filteredRecipes.isEmpty
          ? const Center(child: Text('Keine Rezepte gefunden'))
          : ListView.builder(
              padding: const EdgeInsets.all(16),
              itemCount: filteredRecipes.length,
              itemBuilder: (context, index) {
                final recipe = filteredRecipes[index];
                return RecipeCard(
                  recipe: recipe,
                  isFavorite: favorites.contains(recipe.id),
                  onToggleFavorite: onToggleFavorite,
                );
              },
            ),
    );
  }

  void _showSearchDialog(BuildContext context) {
    showDialog(
      context: context,
      builder: (context) => AlertDialog(
        title: const Text('Suche'),
        content: TextField(
          decoration: const InputDecoration(
            labelText: 'Rezept suchen...',
            prefixIcon: Icon(Icons.search),
          ),
          onChanged: (value) {
            // This would update the search in the parent widget
            // For now, we'll just close the dialog
          },
        ),
        actions: [
          TextButton(
            onPressed: () => Navigator.of(context).pop(),
            child: const Text('Schließen'),
          ),
        ],
      ),
    );
  }
}

class RecipeCard extends StatelessWidget {
  final Recipe recipe;
  final bool isFavorite;
  final Function(String) onToggleFavorite;

  const RecipeCard({
    super.key,
    required this.recipe,
    required this.isFavorite,
    required this.onToggleFavorite,
  });

  @override
  Widget build(BuildContext context) {
    return GestureDetector(
      onTap: () => Navigator.of(context).push(
        MaterialPageRoute(
          builder: (context) => RecipeDetailPage(
            recipe: recipe,
            isFavorite: isFavorite,
            onToggleFavorite: onToggleFavorite,
          ),
        ),
      ),
      child: Card(
        margin: const EdgeInsets.only(bottom: 16),
        elevation: 4,
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(16)),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Stack(
              children: [
                ClipRRect(
                  borderRadius: const BorderRadius.vertical(
                    top: Radius.circular(16),
                  ),
                  child: Image.network(
                    recipe.imageUrl,
                    height: 180,
                    width: double.infinity,
                    fit: BoxFit.cover,
                  ),
                ),
                Positioned(
                  top: 8,
                  right: 8,
                  child: IconButton(
                    icon: Icon(
                      isFavorite ? Icons.favorite : Icons.favorite_border,
                      color: isFavorite ? Colors.red : Colors.white,
                    ),
                    onPressed: () => onToggleFavorite(recipe.id),
                  ),
                ),
              ],
            ),
            Padding(
              padding: const EdgeInsets.all(16),
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    recipe.title,
                    style: Theme.of(context).textTheme.titleLarge,
                  ),
                  const SizedBox(height: 8),
                  Text(
                    recipe.subtitle,
                    style: Theme.of(context).textTheme.bodyMedium,
                  ),
                  const SizedBox(height: 12),
                  Row(
                    children: [
                      Chip(label: Text(recipe.cookTime)),
                      const SizedBox(width: 8),
                      Chip(label: Text(recipe.difficulty)),
                      const SizedBox(width: 8),
                      Chip(label: Text(recipe.category)),
                    ],
                  ),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }
}

